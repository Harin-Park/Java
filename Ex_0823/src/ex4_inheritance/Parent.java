package ex4_inheritance;

public class Parent {
	private int money = 200000000; //부모의 재산
	String str = "송도"; //부모의 부동산
	
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	
	
}
